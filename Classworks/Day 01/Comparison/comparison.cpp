#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 20;
    cout << "a == b: " << (a == b) << endl;  // false = 0
    cout << "a != b: " << (a != b) << endl;  // true = 1
    cout << "a > b: " << (a > b) << endl;    // false = 0
    cout << "a < b: " << (a < b) << endl;    // true = 1
    cout << "a >= b: " << (a >= b) << endl;  // false = 0
    cout << "a <= b: " << (a <= b) << endl;  // true = 1
    return 0;
}
