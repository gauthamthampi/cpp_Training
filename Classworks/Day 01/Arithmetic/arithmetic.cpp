#include <iostream>
using namespace std;

int main(){
    int a = 10, b = 20;
    cout<<"Sum of two numbers:"<<a+b<<endl; //Addition
    cout<<"Difference of two numbers:"<<b-a<<endl; //Subtraction
    cout<<"Multiplying two numbers:"<<a*b<<endl; //Multiplication
    cout<<"Dividing two numbers:"<<b/a<<endl; //Division
    cout<<"Modulus of two numbers:"<<a%b<<endl; //Modulus
    cout<<"Increment of a:"<<++a<<endl; //Increment
    cout<<"Decrement of b:"<<--b<<endl; //Decrement
    return 0;
}